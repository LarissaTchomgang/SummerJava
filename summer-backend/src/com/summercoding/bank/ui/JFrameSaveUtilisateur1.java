    private void buttonOKkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonOKkActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            String login = ChampLogin100.getText();
            String password = ChampPassword100.getText();
            String nom = ChampNom100.getText();
            String prenom = ChampPrenom100.getText();
            int annee = Integer.parseInt(ComboBoxAnnee.getSelectedItem().toString());
            int mois = Integer.parseInt(ComboBoxMois.getSelectedItem().toString());
            int jour = Integer.parseInt(ComboBoxJour.getSelectedItem().toString());
            
            LocalDate DateNaissance = LocalDate.of(annee, mois, jour); 
            
            String  genre = ButtonRadioFeminin.getText();
            if (ButtonRadioMasculin.isSelected())
                genre = ButtonRadioMasculin.getText();
            
            String idAdmin = ComboBoxIdadmin.getSelectedItem().toString().split(" ") [0];
            
            int idadmin = Integer.parseInt(idAdmin) ;
            
            
            controlleur.routeVersSaveUtilisateur(login, password, nom, prenom, DateNaissance, genre, idadmin);
            JOptionPane.showMessageDialog(null, "succes");

            ChampLogin.setText("");
            ChampPassword.setText("");
            ChampNom.setText("");
            ChampPrenom.setText("");
            ChampDateNaissance.setText("");
            ChampGenre.setText("");
            ChampIdAdmin.setText("");

        } catch (SQLException ex) {
            Logger.getLogger(JFrameSaveUtilisateur.class.getName()).log(Level.SEVERE, null, ex);
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "veillez reesayer plus tard");
        }
    }//GEN-LAST:event_buttonOKkActionPerformed

